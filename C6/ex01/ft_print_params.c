#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;
	int j;

	i = 1;
	j = 0;
	argc++;
	while (argv[i])
	{
		while (argv[i][j])
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
		j = 0;
	}
}
