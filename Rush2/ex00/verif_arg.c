int	verif_arg(int argc, char **argv)
{
	if (argc != 2 || argc != 3)
		return (-2);
	else if ((argc == 2 && argv[1] < 0) || (argc == 3 && argv[2] < 0))
		return (-2);
	else if (argc == 3)
		return (2);
	else
		return (1);
}
